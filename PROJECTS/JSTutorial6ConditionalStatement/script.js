let grades = 101;

if (grades >= 101) {

    alert(`Invalid Grades ${grades}`)

} else if (grades >= 98) {

    alert(`With Highest Honor`)

} else if (grades >= 95) {

    alert(`With High Honor`)

} else if (grades >= 90) {

    alert(`With Honor`)

} else if (grades >= 75) {

    alert(`Passed`)

} else {

    alert(`Failed`)
}